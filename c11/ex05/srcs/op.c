int	plus(int value1, int value2)
{
	return (value1 + value2);
}

int	minus(int value1, int value2)
{
	return (value1 - value2);
}

int	multi(int value1, int value2)
{
	return (value1 * value2);
}

int	div(int value1, int value2)
{
	return (value1 / value2);
}

int	mod(int value1, int value2)
{
	return (value1 % value2);
}
