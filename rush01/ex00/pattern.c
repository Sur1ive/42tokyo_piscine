#include <unistd.h>

// n = a * 24^3 + b * 24^2 + c * 24^1 + d * 24^0 : n -> index{a, b, c, d}
// n = 0 ~ 331775 (24^4 - 1)
char	**pattern(int n)
{
	const char	*patterns[24] = {"1234", "1243", "1324", "1342", "1423", "1432",
		"2134", "2143", "2314", "2341", "2413", "2431",
		"3124", "3142", "3214", "3241", "3412", "3421",
		"4123", "4132", "4213", "4231", "4312", "4321"};
	static char	*pattern_n[4];
	int			i;
	int			index[4];

	i = 0;
	index[0] = n / (24 * 24 * 24);
	index[1] = n % (24 * 24 * 24) / (24 * 24);
	index[2] = n % (24 * 24) / 24;
	index[3] = n % 24;
	while (i <= 3)
	{
		pattern_n[i] = (char *)patterns[index[i]];
		i++;
	}
	return (pattern_n);
}

void	print_pattern(char **pattern)
{
	int		i;
	int		j;

	i = 0;
	while (i <= 3)
	{
		j = 0;
		while (j <= 3)
		{
			write(1, &pattern[i][j++], 1);
			if (j < 4)
				write(1, " ", 1);
		}
		i++;
		write(1, "\n", 1);
	}
}
